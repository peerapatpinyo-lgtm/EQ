# Design Response Spectrum สำหรับพื้นที่ดินเหนียวอ่อน  มยผ. 1302-61
# ──────────────────────────────────────────────────────────────────────────────
# สเปกตรัมถูกกำหนดโดยตรงจากมาตรฐาน (ไม่คำนวณจาก Ss/S1 + Fa/Fv)
# รูปร่างสเปกตรัม:
#   T < T0      : Sa = SDS × (0.4 + 0.6 × T/T0)    — ช่วง ascending
#   T0 ≤ T ≤ Ts : Sa = SDS                           — ช่วง plateau คาบสั้น
#   Ts < T ≤ TL : Sa = SDS (flat plateau กว้างพิเศษ เพราะดินอ่อน)
#   T > TL      : Sa = SD1_eff / T                   — ช่วง descending
#
# จังหวัดในกลุ่มดินเหนียวอ่อน: กทม. และปริมณฑล 8 จังหวัด
# ──────────────────────────────────────────────────────────────────────────────

# พารามิเตอร์สเปกตรัม มยผ. 1302-61 แยกตามโซน
