# (มยผ. 1302 กำหนดสเปกตรัมเดียวสำหรับพื้นที่ดินเหนียวอ่อนกรุงเทพฯ)
#
# ค่าพารามิเตอร์:
#   SDS  = 0.20 g   (ความเร่งออกแบบช่วงคาบสั้น)
#   T0   = 0.30 s   (จุดเริ่มต้น plateau)
#   Ts   = 0.30 s   (เท่ากับ T0 เพราะช่วง ascending สั้นมาก)
#   TL   = 1.50 s   (จุดสิ้นสุด plateau — ยาวกว่าปกติมากเพราะดินอ่อนขยาย)
#   SD1_eff = SDS × TL = 0.20 × 1.50 = 0.30 g  (ใช้คำนวณ descending branch)

