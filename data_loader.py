# พารามิเตอร์สเปกตรัม มยผ. 1302 — ค่าคงที่ระบุในมาตรฐาน
SOFT_CLAY_SPECTRUM = {
    "SDS":    0.20,   # g  — ความเร่งออกแบบช่วงคาบสั้น (plateau)
    "T0":     0.30,   # s  — คาบเริ่มต้น plateau
    "TS":     0.30,   # s  — เท่า T0 (ascending ชันสั้น)
    "TL":     1.50,   # s  — คาบสิ้นสุด plateau (long-period transition)
    "SD1_eff": 0.30,  # g  = SDS × TL  ใช้ใน descending branch: Sa = SD1_eff / T
    "source": "มยผ. 1302-61 ตารางที่ 1",
}


def get_soft_clay_sa(T: float) -> float:
    """
    คำนวณ Sa(T) ตามสเปกตรัม มยผ. 1302 สำหรับพื้นที่ดินเหนียวอ่อนกรุงเทพฯ
    T  = คาบเวลา (วินาที)
    Sa = ความเร่งตอบสนอง (g)
    """
    p = SOFT_CLAY_SPECTRUM
    SDS    = p["SDS"]
    T0     = p["T0"]
    TS     = p["TS"]
    TL     = p["TL"]
    SD1eff = p["SD1_eff"]

    if T < 0:
        return 0.0
    elif T < T0:
        # ascending: Sa เริ่มที่ 0.4·SDS เมื่อ T=0 และขึ้นถึง SDS เมื่อ T=T0
        return SDS * (0.4 + 0.6 * T / T0)
    elif T <= TL:
        # flat plateau (ยาวมากสำหรับดินอ่อน)
        return SDS
    else:
        # descending 1/T
        return SD1eff / T


