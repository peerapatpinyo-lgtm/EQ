def get_drift_limit(ie: float) -> tuple[float, str]:
    """คืนค่าขีดจำกัดการโยกตัว (ratio) และ label ตาม Ie"""
    if ie >= 1.5:
        return 0.010, "1.0% hs (อาคารความสำคัญสูงมาก)"
    elif ie >= 1.25:
        return 0.015, "1.5% hs (อาคารความสำคัญสูง)"
    else:
        return 0.020, "2.0% hs (อาคารปกติ)"
